CREATE TABLE T_TEMPCUST
AS
     SELECT ID, C_PERSON, ROWNUM AS ROWID_
      FROM FCB.T_REALCUSTOMER
   ORDER BY ID

SELECT  MAX(ROWID_) +1 FROM T_TEMPCUST --17266

BEGIN
FOR REC IN (
SELECT *  FROM
(SELECT ID,C_PERSON,  ROWNUM ROWID1 FROM T_REALCUSTOMER ) TT)
 LOOP
 UPDATE T_REALCUSTOMER
 SET C_PERSON=(SELECT C_PERSON FROM T_TEMPCUST WHERE ROWID_=17266-REC.ROWID1)
 WHERE ID=REC.ID;
COMMIT;
END LOOP;
END;



CREATE TABLE T_ACCINFO
